<p>In accordance with RBQ Bill 122, the owner of a new building must carry out a thorough inspection of the leveled underground garage structure within 12 to 18 months of the end of construction.</p>
<p>Thereafter, the inspection must also be carried out every five years following the initial inspection. Nevertheless, it stands to reason that in the event that a building shows signs of deterioration that could lead to a dangerous structural problem, it should be inspected immediately.</p>
<p>This is particularly important in northern regions like Quebec. Since structural elements are exposed to the cycle of freezing and thawing as well as conditions that can accelerate corrosion and degradation of structural components. Cars entering the garage bring with them a significant amount of snow and chemicals (salts) which melt once in the warmer environment of the parking lot. The molten salt water can then infiltrate the structural elements and greatly accelerate their corrosion and degradation, thus diminishing their structural capacity.</p>
<p>The bill aims to remedy this problem by requiring an engineer member of the OIQ to carry out an in-depth inspection of any parking lot comprising at least one slab that is not resting on the ground in order to assess the condition of the structural elements and to identify any deficiencies in the systems in place that could lead to faults and damaging conditions.</p>
<p>The building inspectors and the structural engineering team of Groupe SBSE (members of the OIQ), carry out inspections of underground parking in Montreal and the surrounding area,
The Bill 122 Underground Parking Inspection Report provided by our OIQ-certified engineers includes a detailed description of the observations, an assessment of the structural elements and recommendations for correcting the defects observed.</p>
<p>The report provided is a detailed report in accordance with the requirements of the RBQ signed by an engineer member of the Ordre des ingénieurs du Québec (OIQ) and will include the following elements:</p>
